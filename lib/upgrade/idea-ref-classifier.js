/**
 * Module: lib/upgrade/idea-ref-classifier.js
 *
 * Schema-aware classifier for IDEA.md references in artifacts during upgrade.
 * Refines alpha.24's all-or-nothing pre-flight scan into three buckets per
 * ADR-031:
 *
 *   auto_fixable — reference lives in a field Aitri owns (documented in
 *                  ARTIFACTS.md and validated by phaseN.js::validate()).
 *                  Migration applies a mechanical drop (shape transform).
 *                  Currently bound to:
 *                    04_IMPLEMENTATION_MANIFEST.json::files_created[*]
 *                    04_IMPLEMENTATION_MANIFEST.json::files_modified[*]
 *                    04_IMPLEMENTATION_MANIFEST.json::test_files[*]
 *                  Element matches when:
 *                    string element === "IDEA.md"
 *                    object element with .path === "IDEA.md" (Hub's enriched
 *                    shape — phase4.js:40-43 only enforces arrayness)
 *
 *   narrative   — anywhere else: free-form fields (test_data), project
 *                 extensions, narrative bodies (Markdown), free-text strings.
 *                 Aitri does not own these. Flagged for operator review.
 *
 *   frozen      — 04_TEST_RESULTS.json + 05_PROOF_OF_COMPLIANCE.json. These
 *                 are immutable historical records (auto-generated by
 *                 verify-run; Phase 5 evidence). Modifying them falsifies
 *                 history. Counted only; never surfaced as findings.
 *
 * 01_REQUIREMENTS.json is excluded at every level (preserves alpha.24's
 * false-positive guard — original_brief legitimately holds absorbed IDEA.md
 * content as a substring).
 *
 * Feature-directory scanning is read-only. A2 (root upgrade does not cascade
 * to features/.aitri) is a write-side invariant; reading features/<x>/spec/
 * to detect refs and rewriting on-disk content of those spec files does not
 * violate it (the .aitri config of each feature is never touched).
 */

import fs   from 'fs';
import path from 'path';

export const PATTERN = /\bIDEA\.md\b/;

const SKIP_BASENAMES = new Set([
  '01_REQUIREMENTS.json',    // false-positive guard
]);

const FROZEN_BASENAMES = new Set([
  '04_TEST_RESULTS.json',
  '05_PROOF_OF_COMPLIANCE.json',
]);

const ROOT_NAMES = [
  '00_DISCOVERY.md', '01_UX_SPEC.md',
  '02_SYSTEM_DESIGN.md', '03_TEST_CASES.json',
  '04_IMPLEMENTATION_MANIFEST.json', '04_TEST_RESULTS.json',
  '04_CODE_REVIEW.md', '05_PROOF_OF_COMPLIANCE.json',
  'BUGS.json', 'BACKLOG.json', 'AUDIT_REPORT.md',
];

const AUTO_FIX_FIELD_PATH = /^(files_created|files_modified|test_files)\[\d+\](\.path)?$/;

// ── String walk over arbitrary JSON ──────────────────────────────────────────

function* walkStrings(node, prefix = '') {
  if (typeof node === 'string') {
    yield { fieldPath: prefix, value: node };
  } else if (Array.isArray(node)) {
    for (let i = 0; i < node.length; i++) {
      yield* walkStrings(node[i], prefix + '[' + i + ']');
    }
  } else if (node && typeof node === 'object') {
    for (const k of Object.keys(node)) {
      const next = prefix ? prefix + '.' + k : k;
      yield* walkStrings(node[k], next);
    }
  }
}

// ── Per-artifact classifiers ─────────────────────────────────────────────────

function classifyManifest(absPath, fileRel) {
  const auto = [], narr = [];
  let data;
  try { data = JSON.parse(fs.readFileSync(absPath, 'utf8')); }
  catch { return { auto, narr }; }

  for (const { fieldPath, value } of walkStrings(data)) {
    if (!PATTERN.test(value)) continue;
    if (AUTO_FIX_FIELD_PATH.test(fieldPath) && value === 'IDEA.md') {
      auto.push({ file: fileRel, fieldPath, value });
    } else {
      narr.push({ file: fileRel, fieldPath, value });
    }
  }

  // Degenerate-state guard: reclassify auto → narrative if the post-fix
  // manifest would fail phase4.js:40-43 (both files_created and
  // files_modified empty). Aitri refuses to produce a manifest the validator
  // would reject; the operator decides instead.
  if (auto.length > 0) {
    try {
      simulateAutoFix(data, auto);
    } catch {
      narr.push(...auto);
      auto.length = 0;
    }
  }

  return { auto, narr };
}

function classifyGenericJSON(absPath, fileRel) {
  const narr = [];
  let data;
  try { data = JSON.parse(fs.readFileSync(absPath, 'utf8')); }
  catch { return { auto: [], narr }; }

  for (const { fieldPath, value } of walkStrings(data)) {
    if (PATTERN.test(value)) narr.push({ file: fileRel, fieldPath, value });
  }
  return { auto: [], narr };
}

function classifyMarkdown(absPath, fileRel) {
  const narr = [];
  let raw;
  try { raw = fs.readFileSync(absPath, 'utf8'); }
  catch { return { auto: [], narr }; }
  if (PATTERN.test(raw)) {
    narr.push({ file: fileRel, fieldPath: 'body', value: '<markdown body>' });
  }
  return { auto: [], narr };
}

function frozenHits(absPath) {
  let raw;
  try { raw = fs.readFileSync(absPath, 'utf8'); }
  catch { return 0; }
  return PATTERN.test(raw) ? 1 : 0;
}

function dispatchClassify(absPath, fileRel) {
  const base = path.basename(absPath);
  if (SKIP_BASENAMES.has(base)) return null;
  if (FROZEN_BASENAMES.has(base)) {
    return { auto: [], narr: [], frozen: frozenHits(absPath) };
  }
  if (base === '04_IMPLEMENTATION_MANIFEST.json') {
    return { ...classifyManifest(absPath, fileRel), frozen: 0 };
  }
  if (base.endsWith('.json')) {
    return { ...classifyGenericJSON(absPath, fileRel), frozen: 0 };
  }
  if (base.endsWith('.md')) {
    return { ...classifyMarkdown(absPath, fileRel), frozen: 0 };
  }
  return null;
}

// ── Driver ──────────────────────────────────────────────────────────────────

/**
 * Classify all IDEA.md references reachable from the project root.
 *
 * @param {string} dir Project root.
 * @param {object} config The .aitri config (used for artifactsDir).
 * @returns {{ autoFixable: Array<Ref>, narrative: Array<Ref>, frozenCount: number }}
 *   Ref shape: { file: string (relative), fieldPath: string, value: string }
 */
export function classifyIdeaReferences(dir, config) {
  const autoFixable = [];
  const narrative   = [];
  let frozenCount   = 0;

  const accept = (absPath, fileRel) => {
    if (!fs.existsSync(absPath)) return;
    const r = dispatchClassify(absPath, fileRel);
    if (!r) return;
    autoFixable.push(...r.auto);
    narrative.push(...r.narr);
    frozenCount += r.frozen;
  };

  // Root pipeline.
  const artifactsDir = config.artifactsDir || '';
  for (const n of ROOT_NAMES) {
    const abs = path.join(dir, artifactsDir, n);
    const fileRel = path.relative(dir, abs);
    accept(abs, fileRel);
  }

  // Feature pipelines — fixed `spec/` subdirectory (mirrors alpha.24 scan
  // boundary; A2 is a write-side invariant, scanning is read-only).
  const featuresDir = path.join(dir, 'features');
  if (!fs.existsSync(featuresDir)) {
    return { autoFixable, narrative, frozenCount };
  }
  let stat;
  try { stat = fs.statSync(featuresDir); } catch { return { autoFixable, narrative, frozenCount }; }
  if (!stat.isDirectory()) return { autoFixable, narrative, frozenCount };

  let entries = [];
  try { entries = fs.readdirSync(featuresDir, { withFileTypes: true }); }
  catch { /* unreadable */ }

  for (const e of entries) {
    if (!e.isDirectory()) continue;
    const featSpec = path.join(featuresDir, e.name, 'spec');
    if (!fs.existsSync(featSpec)) continue;
    let files = [];
    try { files = fs.readdirSync(featSpec); } catch { continue; }
    for (const fn of files) {
      if (!/\.(md|json)$/.test(fn)) continue;
      const abs = path.join(featSpec, fn);
      const fileRel = path.posix.join('features', e.name, 'spec', fn);
      accept(abs, fileRel);
    }
  }

  return { autoFixable, narrative, frozenCount };
}

// ── Auto-fix application ────────────────────────────────────────────────────

/**
 * Mutates `data` in place: drops elements at the indices implied by `refs`
 * from files_created / files_modified / test_files. Throws if the post-drop
 * manifest would leave both files_created and files_modified empty (a state
 * phase4.js:40-43 rejects).
 *
 * Drop order is descending by index to preserve positions of remaining
 * elements as we splice.
 */
function simulateAutoFix(data, refs) {
  const indicesByArray = {};
  for (const r of refs) {
    const m = r.fieldPath.match(/^(files_created|files_modified|test_files)\[(\d+)\]/);
    if (!m) continue;
    const [, arr, idx] = m;
    if (!indicesByArray[arr]) indicesByArray[arr] = new Set();
    indicesByArray[arr].add(Number(idx));
  }

  for (const [arr, idxSet] of Object.entries(indicesByArray)) {
    if (!Array.isArray(data[arr])) continue;
    const sorted = [...idxSet].sort((a, b) => b - a);
    for (const i of sorted) data[arr].splice(i, 1);
  }

  const hasCreated  = Array.isArray(data.files_created)  && data.files_created.length  > 0;
  const hasModified = Array.isArray(data.files_modified) && data.files_modified.length > 0;
  if (!hasCreated && !hasModified) {
    throw new Error('post-drop manifest would have empty files_created and files_modified');
  }
}

/**
 * Apply auto-fix to a manifest file on disk. Returns the new content as a
 * string (caller writes it). Throws on degenerate result so the diagnose
 * step can re-classify the refs as narrative — this should not happen in
 * practice because classifyManifest already simulates and reclassifies; this
 * is defensive belt+suspenders.
 *
 * @param {string} absPath Absolute path to the manifest.
 * @param {Array<Ref>} refs Refs targeting this file (auto_fixable bucket).
 * @returns {string} New JSON content (2-space indent — matches existing
 *   migrations' output convention).
 */
export function applyManifestAutoFix(absPath, refs) {
  const data = JSON.parse(fs.readFileSync(absPath, 'utf8'));
  simulateAutoFix(data, refs);
  return JSON.stringify(data, null, 2);
}

/**
 * Mapping artifact basename → phase key for artifactHashes re-stamp. Only
 * artifacts whose auto-fixable map is registered above appear here. New
 * auto-fixable artifacts must be added to both AUTO_FIX_FIELD_PATH semantics
 * and this map.
 */
const ARTIFACT_PHASE = {
  '04_IMPLEMENTATION_MANIFEST.json': '4',
};

export function phaseForArtifact(fileRel) {
  return ARTIFACT_PHASE[path.basename(fileRel)] || null;
}
